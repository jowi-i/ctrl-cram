    private void updateAttendanceTable() {
    if (selectedEmployeeId == null || selectedEmployeeId.isEmpty()) {
        return; 
    }

    String selectedMonth = (String) comboMonth.getSelectedItem();
    String selectedYear = (String) comboYear.getSelectedItem();

    
    DateTimeFormatter inputFormat = DateTimeFormatter.ofPattern("MMMM", Locale.ENGLISH);
    DateTimeFormatter outputFormat = DateTimeFormatter.ofPattern("MM");
    String monthNumber = outputFormat.format(inputFormat.parse(selectedMonth));

    List<Attendance> records = employeeManager.getAttendanceForEmployee(selectedEmployeeId, monthNumber, selectedYear);

    DefaultTableModel model = (DefaultTableModel) tblAttendance.getModel();
    model.setRowCount(0); 

    int totalDays = records.size();
    int presentCount = 0;
    int absentCount = 0;
    int lateCount = 0;
    int totalOvertimeMinutes = 0;
    int totalWorkedMinutes = 0;

    for (Attendance record : records) {
        String[] analysis = AttendanceTracker.analyze(record);
        String status = analysis[0].toLowerCase();
        
        model.addRow(new Object[]{
            record.getDate().toString(),
            analysis[0],  // Status: Present / Late / Absent
            analysis[1],  // Time In
            analysis[2],  // Time Out
            analysis[3],  // Overtime
            analysis[4]   // Total Hours
        });

        
    if (!status.contains("absent") && !status.contains("missed log")) {
        presentCount++;
}

    // Count as late if "late" is mentioned in status <3
    if (status.contains("late")) {
        lateCount++;
}
    if (record.getTimeIn() != null && record.getTimeOut() != null) {
    Duration worked = Duration.between(record.getTimeIn(), record.getTimeOut());
    totalWorkedMinutes += worked.toMinutes();
}



        if (!analysis[3].equals("00:00")) {
                String[] parts = analysis[3].split(":");
                int hours = Integer.parseInt(parts[0]);
                int minutes = Integer.parseInt(parts[1]);
                totalOvertimeMinutes += (hours * 60 + minutes);
                
                
}
}
        int otHours = totalOvertimeMinutes / 60;
        int otMinutes = totalOvertimeMinutes % 60;
        String totalOvertimeFormatted = String.format("%02d:%02d", otHours, otMinutes);
        int workedHours = totalWorkedMinutes / 60;
        int workedMinutes = totalWorkedMinutes % 60;
        String totalWorkedFormatted = String.format("%02d:%02d", workedHours, workedMinutes);


        
// changes ... 
    lblDaysPresent.setText( presentCount + " / " + totalDays);
    lblDaysAbsent.setText( absentCount + " / " + totalDays);
    lblLate.setText("" + lateCount);
    lblOT.setText( totalOvertimeFormatted);
    lblTotalWorked.setText(totalWorkedFormatted);
}

  



    

    
